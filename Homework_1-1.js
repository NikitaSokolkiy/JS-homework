// 7. Определение типа треугольника:
//     Запросите у пользователя длины трех сторон треугольника.
//     Определите, является ли треугольник равносторонним, равнобедренным, прямоугольным или обычным.
// НЕ ПОНЯЛ ЧАСТИЧНО РЕШЕНИЕ

let side1 = Number(prompt("Надо вычислить тип треугольника. Введите сторону x"));
let side2 = Number(prompt("Надо вычислить тип треугольника. Введите сторону y"));
let side3 = Number(prompt("Надо вычислить тип треугольника. Введите сторону z"));

if (side1 == side2 || side2 == side3 || side3 == side1){
    console.log("Треугольник равнобедренный");
}else if (side1 == side2 && side2 == side3 ){
    console.log("Треугольник равносторонний");
}else {
    // НАЧИНАЯ ОТ СЮДА
    const sides = [side1, side2, side3].sort((r, t) => r - t);
    if (Math.pow(sides[0], 2) + Math.pow(sides[1], 2) + Math.pow(sides[2], 2)) {
        console.log("Треугольник прямоугольный.");
    } else {
        console.log("Треугольник обычный.");
    }
}

