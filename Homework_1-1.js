// 5. Проверка на квадратное уравнение:
//     Запросите у пользователя коэффициенты a, b и c квадратного уравнения ax^2 + bx + c = 0.
//     Определите, имеет ли уравнение действительные корни, и выведите их, если они есть.

let a = Number(prompt("Перелайте данное для квадратного уравнения (a)"));
let b = Number(prompt("Перелайте данное для квадратного уравнения (b)"));
let c = Number(prompt("Перелайте данное для квадратного уравнения (c)"));

const discriminant = b * b - 4 * a * c

if (discriminant > 0 ){
    const x1 = (-b + Math.sqrt(discriminant))/(2 * a);
    const x2 = (-b - Math.sqrt(discriminant))/(2 * a);

    console.log(`Решение уравнения являются корни ${x1} и ${x2}`)
} else if (discriminant == 0) {
    // Один вещественный корень
    const x = -b / (2 * a);
    console.log(`У уравнения ${a}x^2 + ${b}x + ${c} есть один вещественный корень: x = ${x}`);
} else {
    console.log(`У уравнения ${a}x^2 + ${b}x + ${c} нет  корней`);
}


